# How many hours in a year
hours_in_a_day = 24
days_in_a_year = 365

hours_in_a_year = hours_in_a_day * days_in_a_year
puts hours_in_a_year

# Minutes in a decade
# Minutes in a year * 10
minutes_in_a_day = 60 * 24
minutes_in_a_year = minutes_in_a_day * 365
minutes_in_a_decade = minutes_in_a_year * 10
puts minutes_in_a_decade

#Age in seconds
minutes_in_a_year *= 60
puts minutes_in_a_year * 25

# How old am I?
# 1246 million seconds
age_in_seconds = 1_246_000_000

puts age_in_seconds / minutes_in_a_year # 39 years old
